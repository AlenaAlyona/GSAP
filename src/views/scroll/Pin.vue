<script>
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";
import spartan from "@/assets/scroll/pin/spartan.svg";
import castle from "@/assets/scroll/pin/castle.svg";
import daVinci from "@/assets/scroll/pin/da-vinci.svg";
import airplane from "@/assets/scroll/pin/airplane.svg";

gsap.registerPlugin(ScrollTrigger);

export default {
  name: "Pin",

  data() {
    return {
      src: [spartan, castle, daVinci, airplane],

      dates: ["-3300 | 476", "476 | 1492", "1492 | 1792", "1792 | AJD"],
    };
  },

  mounted() {
    ScrollTrigger.create({
      trigger: ".pin-sections",
      pin: ".pin-tooltip",
      start: "top 40%",
      end: "bottom 55%",
    });

    const sections = document.querySelectorAll(".pin-section");

    sections.forEach((section, index) => {
      console.log(index);
      ScrollTrigger.create({
        trigger: section,
        start: "top center",
        end: "bottom center",
        onEnter: () => {
          gsap.set(".pin-tooltip-img", {
            attr: { src: this.src[index] },
          });
          gsap.set(".pin-tooltip p", {
            innerText: this.dates[index],
          });
        },
        onEnterBack: () => {
          gsap.set(".pin-tooltip-img", {
            attr: { src: this.src[index] },
          });
          gsap.set(".pin-tooltip p", {
            innerText: this.dates[index],
          });
        },
      });
    });
  },
};
